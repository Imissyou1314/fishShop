package com.zhanjixun.domain2;

public class Good {
	private String goodsId;
	private String goodsName;
	private String goodsPhoto;
	private Double price;
	private String skuId;
	private String unit;
	private Integer number;
	private Integer sellNumber;
	private String skuString;

	/**
	 * @return goodsId
	 */
	public String getGoodsId() {
		return goodsId;
	}

	/**
	 * @param goodsId
	 *            要设置的 goodsId
	 */
	public void setGoodsId(String goodsId) {
		this.goodsId = goodsId;
	}

	/**
	 * @return goodsName
	 */
	public String getGoodsName() {
		return goodsName;
	}

	/**
	 * @param goodsName
	 *            要设置的 goodsName
	 */
	public void setGoodsName(String goodsName) {
		this.goodsName = goodsName;
	}

	/**
	 * @return fishPhoto
	 */
	public String getGoodsPhoto() {
		return goodsPhoto;
	}

	/**
	 * @param fishPhoto
	 *            要设置的 fishPhoto
	 */
	public void setGoodsPhoto(String fishPhoto) {
		this.goodsPhoto = fishPhoto;
	}

	/**
	 * @return price
	 */
	public Double getPrice() {
		return price;
	}

	/**
	 * @param price
	 *            要设置的 price
	 */
	public void setPrice(Double price) {
		this.price = price;
	}

	/**
	 * @return sku
	 */
	public String getSkuId() {
		return skuId;
	}

	/**
	 * @param sku
	 *            要设置的 sku
	 */
	public void setSkuId(String sku) {
		this.skuId = sku;
	}

	/**
	 * @return unit
	 */
	public String getUnit() {
		return unit;
	}

	/**
	 * @param unit
	 *            要设置的 unit
	 */
	public void setUnit(String unit) {
		this.unit = unit;
	}

	/**
	 * @return number
	 */
	public Integer getNumber() {
		return number;
	}

	/**
	 * @param number
	 *            要设置的 number
	 */
	public void setNumber(Integer number) {
		this.number = number;
	}

	/**
	 * @return sellNumber
	 */
	public Integer getSellNumber() {
		return sellNumber;
	}

	/**
	 * @param sellNumber
	 *            要设置的 sellNumber
	 */
	public void setSellNumber(Integer sellNumber) {
		this.sellNumber = sellNumber;
	}

	/**
	 * @return skuString
	 */
	public String getSkuString() {
		return skuString;
	}

	/**
	 * @param skuString 要设置的 skuString
	 */
	public void setSkuString(String skuString) {
		this.skuString = skuString;
	}
}
