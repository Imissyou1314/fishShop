package com.zhanjixun.domain2;


public class GoodListItem {
	private String categoryId;
	private String categorySimpleName;
	private String categoryAcademicName;
	private String categoryEnglishName;
	private String fishPhoto;
	private Integer totalSellNumber;
	private Float lowPrice;
	private String unit;
	/**
	 * @return categoryId
	 */
	public String getCategoryId() {
		return categoryId;
	}
	/**
	 * @param categoryId 要设置的 categoryId
	 */
	public void setCategoryId(String categoryId) {
		this.categoryId = categoryId;
	}
	/**
	 * @return categorySimpleName
	 */
	public String getCategorySimpleName() {
		return categorySimpleName;
	}
	/**
	 * @param categorySimpleName 要设置的 categorySimpleName
	 */
	public void setCategorySimpleName(String categorySimpleName) {
		this.categorySimpleName = categorySimpleName;
	}
	/**
	 * @return categoryAcademicName
	 */
	public String getCategoryAcademicName() {
		return categoryAcademicName;
	}
	/**
	 * @param categoryAcademicName 要设置的 categoryAcademicName
	 */
	public void setCategoryAcademicName(String categoryAcademicName) {
		this.categoryAcademicName = categoryAcademicName;
	}
	/**
	 * @return categoryEnglishName
	 */
	public String getCategoryEnglishName() {
		return categoryEnglishName;
	}
	/**
	 * @param categoryEnglishName 要设置的 categoryEnglishName
	 */
	public void setCategoryEnglishName(String categoryEnglishName) {
		this.categoryEnglishName = categoryEnglishName;
	}
	/**
	 * @return fishPhoto
	 */
	public String getFishPhoto() {
		return fishPhoto;
	}
	/**
	 * @param fishPhoto 要设置的 fishPhoto
	 */
	public void setFishPhoto(String fishPhoto) {
		this.fishPhoto = fishPhoto;
	}
	/**
	 * @return totalSellNumber
	 */
	public Integer getTotalSellNumber() {
		return totalSellNumber;
	}
	/**
	 * @param totalSellNumber 要设置的 totalSellNumber
	 */
	public void setTotalSellNumber(Integer totalSellNumber) {
		this.totalSellNumber = totalSellNumber;
	}
	/**
	 * @return lowPrice
	 */
	public Float getLowPrice() {
		return lowPrice;
	}
	/**
	 * @param lowPrice 要设置的 lowPrice
	 */
	public void setLowPrice(Float lowPrice) {
		this.lowPrice = lowPrice;
	}
	/**
	 * @return unit
	 */
	public String getUnit() {
		return unit;
	}
	/**
	 * @param unit 要设置的 unit
	 */
	public void setUnit(String unit) {
		this.unit = unit;
	}
}
